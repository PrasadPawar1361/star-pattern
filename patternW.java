public class patternW {
     public static void main(String[] args){

        int height =6;

        for(int i = 0; i<=height; i++){
            
            if(i == 3){
                System.out.println("*     *     *");
            }
             
            else if(i == 4){
               
                System.out.println("*   *   *   *"); 
            }
            else if(i == 5){
               
                System.out.println("*  *     *  *"); 
            }
            else if(i == 6){
               
                System.out.println("* *       * *"); 
            }
            
            else{
                               
                System.out.println("*           *"); 
            }
            }

                     
        }
    }